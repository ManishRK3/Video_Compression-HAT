import cv2
import numpy as np
import os.path as osp
from torch.utils import data as data
from torchvision.transforms.functional import normalize

from hat.data.data_util import paths_from_lmdb, scandir
from hat.data.transforms import augment
from hat.utils import FileClient, imfrombytes, img2tensor
from hat.utils.matlab_functions import imresize
from hat.utils.color_util import rgb2ycbcr
from hat.utils.registry import DATASET_REGISTRY

@DATASET_REGISTRY.register()
class ImageNetPairedDatasetFullFrame(data.Dataset):
    """
    Paired dataset for super-resolution, but returns the full frame (e.g., 360x640) instead of square crops.
    Assumes LQ is generated by downsampling GT.
    """
    def __init__(self, opt):
        super(ImageNetPairedDatasetFullFrame, self).__init__()
        self.opt = opt
        self.file_client = None
        self.io_backend_opt = opt['io_backend']
        self.mean = opt['mean'] if 'mean' in opt else None
        self.std = opt['std'] if 'std' in opt else None
        self.gt_folder = opt['dataroot_gt']

        if self.io_backend_opt['type'] == 'lmdb':
            self.io_backend_opt['db_paths'] = [self.gt_folder]
            self.io_backend_opt['client_keys'] = ['gt']
            self.paths = paths_from_lmdb(self.gt_folder)
        elif 'meta_info_file' in self.opt:
            with open(self.opt['meta_info_file'], 'r') as fin:
                self.paths = [osp.join(self.gt_folder, line.split(' ')[0]) for line in fin]
        else:
            self.paths = sorted(list(scandir(self.gt_folder, full_path=True)))

    def __getitem__(self, index):
        if self.file_client is None:
            self.file_client = FileClient(self.io_backend_opt.pop('type'), **self.io_backend_opt)

        scale = self.opt['scale']
        gt_path = self.paths[index]
        img_bytes = self.file_client.get(gt_path, 'gt')
        img_gt = imfrombytes(img_bytes, float32=True)

        # modcrop to ensure divisible by scale
        size_h, size_w, _ = img_gt.shape
        size_h = size_h - size_h % scale
        size_w = size_w - size_w % scale
        img_gt = img_gt[0:size_h, 0:size_w, :]

        # If gt_size is set, crop random patch for patch-based training
        if 'gt_size' in self.opt and self.opt['gt_size'] is not None:
            gt_size = self.opt['gt_size']
            h, w = img_gt.shape[:2]
            if h > gt_size and w > gt_size:
                top = np.random.randint(0, h - gt_size + 1)
                left = np.random.randint(0, w - gt_size + 1)
                img_gt = img_gt[top:top+gt_size, left:left+gt_size, :]
            # else: if image is smaller, no crop (could pad if needed)
            # After cropping, ensure divisibility by scale
            size_h, size_w, _ = img_gt.shape
            size_h = size_h - size_h % scale
            size_w = size_w - size_w % scale
            img_gt = img_gt[0:size_h, 0:size_w, :]
        else:
            # Pad to make height and width divisible by window_size
            window_size = self.opt.get('window_size', 16)
            pad_h = (window_size - (img_gt.shape[0] % window_size)) % window_size
            pad_w = (window_size - (img_gt.shape[1] % window_size)) % window_size
            if pad_h > 0 or pad_w > 0:
                img_gt = cv2.copyMakeBorder(img_gt, 0, pad_h, 0, pad_w, cv2.BORDER_REFLECT_101)

        # Resize to target size if specified (optional)
        if 'target_size' in self.opt:
            target_h, target_w = self.opt['target_size']
            img_gt = cv2.resize(img_gt, (target_w, target_h))
            size_h, size_w = target_h, target_w

        # Generate LQ by downsampling
        img_lq = imresize(img_gt, 1 / scale)
        img_gt = np.ascontiguousarray(img_gt, dtype=np.float32)
        img_lq = np.ascontiguousarray(img_lq, dtype=np.float32)

        # Augmentation (flip, rotation)
        if self.opt['phase'] == 'train':
            img_gt, img_lq = augment([img_gt, img_lq], self.opt['use_hflip'], self.opt['use_rot'])

        # Color space transform
        if 'color' in self.opt and self.opt['color'] == 'y':
            img_gt = rgb2ycbcr(img_gt, y_only=True)[..., None]
            img_lq = rgb2ycbcr(img_lq, y_only=True)[..., None]

        # BGR to RGB, HWC to CHW, numpy to tensor
        img_gt, img_lq = img2tensor([img_gt, img_lq], bgr2rgb=True, float32=True)
        # Normalize
        if self.mean is not None or self.std is not None:
            normalize(img_lq, self.mean, self.std, inplace=True)
            normalize(img_gt, self.mean, self.std, inplace=True)

        return {'lq': img_lq, 'gt': img_gt, 'gt_path': gt_path}

    def __len__(self):
        return len(self.paths)
